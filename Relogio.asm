; --- Mapeamento de Hardware (8051) ---
    RS      equ     P1.3    ;Reg Select ligado em P1.3
    EN      equ     P1.2    ;Enable ligado em P1.2


org 0000h
	LJMP START

org 0030h
START:
    MOV R5, #00	;Inicia Horas
          	
    MOV R3, #50  ; Inicia minutos
    ACALL lcd_init           

   ;Timer 
    MOV TMOD, #01h           
    MOV TH0, #3Ch            
    MOV TL0, #0B0h          
    SETB TR0                  ; Inicia Timer 

LOOP:
    ; Exibir valor hora R5
    MOV A, #06h               
    ACALL posicionaCursor      
    MOV A, R5
    MOV B, #10
    DIV AB                    
    ADD A, #30h               
    ACALL sendCharacter       
    MOV A, B
    ADD A, #30h               
    ACALL sendCharacter       
		  
    ; Exibir valor de minutos r3
    MOV A, #08h               
    ACALL posicionaCursor      
    MOV A, R3
    MOV B, #10
    DIV AB                    
    ADD A, #30h               
    ACALL sendCharacter       
    MOV A, B
    ADD A, #30h               
    ACALL sendCharacter       
   
    JNB TF0, LOOP             
    CLR TF0                   
    MOV TH0, #3Ch             
    MOV TL0, #0B0h
    INC R3                    ; Adiciona minutos

  
    CJNE R3, #60, LOOP
    MOV R3, #00               ; Volta minutos para 00
    INC R5                    ; Adiciona 1 hr
    CJNE R5, #24, LOOP
    MOV R5, #00               ; Volta para 00 horas 
		
    JMP LOOP                  



; initialise the display
; see instruction set for details
lcd_init:

	CLR RS		; clear RS - indicates that instructions are being sent to the module

; function set	
	CLR P1.7		; |
	CLR P1.6		; |
	SETB P1.5		; |
	CLR P1.4		; | high nibble set

	SETB EN		; |
	CLR EN		; | negative edge on E

	CALL delay		; wait for BF to clear	
					; function set sent for first time - tells module to go into 4-bit mode
; Why is function set high nibble sent twice? See 4-bit operation on pages 39 and 42 of HD44780.pdf.

	SETB EN		; |
	CLR EN		; | negative edge on E
					; same function set high nibble sent a second time

	SETB P1.7		; low nibble set (only P1.7 needed to be changed)

	SETB EN		; |
	CLR EN		; | negative edge on E
				; function set low nibble sent
	CALL delay		; wait for BF to clear


; entry mode set
; set to increment with no shift
	CLR P1.7		; |
	CLR P1.6		; |
	CLR P1.5		; |
	CLR P1.4		; | high nibble set

	SETB EN		; |
	CLR EN		; | negative edge on E

	SETB P1.6		; |
	SETB P1.5		; |low nibble set

	SETB EN		; |
	CLR EN		; | negative edge on E

	CALL delay		; wait for BF to clear


; display on/off control
; the display is turned on, the cursor is turned on and blinking is turned on
	CLR P1.7		; |
	CLR P1.6		; |
	CLR P1.5		; |
	CLR P1.4		; | high nibble set

	SETB EN		; |
	CLR EN		; | negative edge on E

	SETB P1.7		; |
	SETB P1.6		; |
	SETB P1.5		; |
	SETB P1.4		; | low nibble set

	SETB EN		; |
	CLR EN		; | negative edge on E

	CALL delay		; wait for BF to clear
	RET


sendCharacter:
	SETB RS  		; setb RS - indicates that data is being sent to module
	MOV C, ACC.7		; |
	MOV P1.7, C			; |
	MOV C, ACC.6		; |
	MOV P1.6, C			; |
	MOV C, ACC.5		; |
	MOV P1.5, C			; |
	MOV C, ACC.4		; |
	MOV P1.4, C			; | high nibble set

	SETB EN			; |
	CLR EN			; | negative edge on E

	MOV C, ACC.3		; |
	MOV P1.7, C			; |
	MOV C, ACC.2		; |
	MOV P1.6, C			; |
	MOV C, ACC.1		; |
	MOV P1.5, C			; |
	MOV C, ACC.0		; |
	MOV P1.4, C			; | low nibble set

	SETB EN			; |
	CLR EN			; | negative edge on E

	CALL delay			; wait for BF to clear
	RET

